# Create vectors with numbers of your choice
numbers1 <- c(3, 7, 11, 15)
numbers2 <- c(2, 4, 6, 8)

# Calculate the sum of each vector
sum1 <- sum(numbers1)
sum2 <- sum(numbers2)

# Calculate the mean of each vector
mean1 <- mean(numbers1)
mean2 <- mean(numbers2)

# Display the results
cat("Sum of numbers1:", sum1, "\n")
cat("Mean of numbers1:", mean1, "\n")
cat("Sum of numbers2:", sum2, "\n")
cat("Mean of numbers2:", mean2, "\n")
