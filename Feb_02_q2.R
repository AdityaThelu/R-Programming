# Create two vectors A and B
A <- c(5, 10, 15, 20)
B <- c(2, 4, 6, 8)

# Perform arithmetic operations element-wise
sum  <- A + B
diff <- A - B
prod <- A * B
quot <- A / B

# Prompt the output on the console
cat("Sum of A and B:\n", sum, "\n")
cat("Difference of A and B:\n", diff, "\n")
cat("Product of A and B:\n", prod, "\n")
cat("Quotient of A and B:\n", quot, "\n") 